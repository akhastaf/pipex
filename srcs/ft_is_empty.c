#include "../include/pipex.h"

int	ft_is_empty(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (0);
	while (s && s[i] && (s[i] == ' ' || s[i] == '\t'))
		i++;
	if (s && s[i])
		return (0);
	return (1);
}
